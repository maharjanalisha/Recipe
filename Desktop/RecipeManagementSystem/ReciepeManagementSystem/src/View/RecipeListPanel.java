    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Select a recipe to delete");
        return;
    }
    int confirm = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete this recipe?",
        "Confirm Delete",
        javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        RecipeController.deleteRecipe(selectedRow);
        loadRecipes();
    }

  
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField1.getText().trim();

    // If search field is empty â†’ show all recipes
    if (keyword.isEmpty() || keyword.equalsIgnoreCase("Search Recipe")) {
        loadRecipes();
        return;
    }

    java.util.List<Recipe> results =
            RecipeController.linearSearchByName(keyword);


    // If no results found
    if (results.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "No recipes found");
        loadRecipes(); // reset table
        return;
    }

    // Show filtered results
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    for (Recipe r : results) {
        model.addRow(new Object[]{
        r.getName(),
        r.getCategory(),
        r.getTime(),
        r.getDifficulty()
    });

    }

    }//GEN-LAST:event_jButton1ActionPerformed

